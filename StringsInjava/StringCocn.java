public class StringCocn {

    public static void main(String[] args) {
        
        char ch[]={'a','b','c','d','e'};

        for(int i=0;i<ch.length;i++){
            System.out.print(ch[i]+" ");
        }
        System.out.println();


        String str="My name is Tony Stark";
        for(int i=0;i<str.length();i++){
            System.out.print(str.charAt(i)+"      ");
        }
    }
    
}
